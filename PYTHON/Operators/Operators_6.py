# Operators in Python are symbols or special keywords that allow you to perform various operations on variables and values. Python supports a wide range of operators, which can be categorized into several groups:

# 1. **Arithmetic Operators:**
#    - `+` (Addition)
#    - `-` (Subtraction)
#    - `*` (Multiplication)
#    - `/` (Division)
#    - `%` (Modulus, remainder of division)
#    - `**` (Exponentiation)
#    - `//` (Floor Division, division with truncating the decimal part)

# 2. **Comparison Operators:**
#    - `==` (Equal to)
#    - `!=` (Not equal to)
#    - `<` (Less than)
#    - `>` (Greater than)
#    - `<=` (Less than or equal to)
#    - `>=` (Greater than or equal to)

# 3. **Logical Operators:**
#    - `and` (Logical AND)
#    - `or` (Logical OR)
#    - `not` (Logical NOT)

# 4. **Assignment Operators:**
#    - `=` (Assign)
#    - `+=` (Add and assign)
#    - `-=` (Subtract and assign)
#    - `*=` (Multiply and assign)
#    - `/=` (Divide and assign)
#    - `%=` (Modulus and assign)
#    - `**=` (Exponentiate and assign)
#    - `//=` (Floor divide and assign)

# 5. **Bitwise Operators:**
#    - `&` (Bitwise AND)
#    - `|` (Bitwise OR)
#    - `^` (Bitwise XOR)
#    - `~` (Bitwise NOT)
#    - `<<` (Left shift)
#    - `>>` (Right shift)

# 6. **Membership Operators:**
#    - `in` (Checks if an element is present in a sequence)
#    - `not in` (Checks if an element is not present in a sequence)

# 7. **Identity Operators:**
#    - `is` (Checks if two objects are the same)
#    - `is not` (Checks if two objects are not the same)

# 8. **Ternary Operator:**
#    - `x if condition else y` (Conditional expression)

# 9. **Unary Operators:**
#    - `-` (Unary minus, negation)
#    - `+` (Unary plus)
#    - `~` (Bitwise NOT)

# These operators can be used in expressions to perform various calculations, comparisons, and logical operations. Here are some examples:

# ```python
# # Arithmetic Operators
# result = 10 + 5  # Addition
# result = 20 - 7  # Subtraction
# result = 5 * 4   # Multiplication
# result = 15 / 3  # Division

# # Comparison Operators
# x = 10
# y = 20
# result = x == y  # False, because x is not equal to y

# # Logical Operators
# a = True
# b = False
# result = a and b  # False, because both a and b are not True

# # Assignment Operators
# count = 0
# count += 1  # Increment count by 1

# # Membership Operators
# numbers = [1, 2, 3, 4, 5]
# result = 3 in numbers  # True, because 3 is in the list

# # Identity Operators
# x = [1, 2, 3]
# y = x
# result = x is y  # True, because both x and y reference the same object
# ```

